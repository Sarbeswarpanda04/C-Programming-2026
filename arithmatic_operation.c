#include<stdio.h>
int main(){
    int x = 5;
    int y = 2;
    printf("Initial values are x=%d and y=%d", x, y);
    printf("the addition of %d & %d is %d", x, y, x + y);
    printf("\nthe subtraction of %d & %d is %d", x, y, x - y);
    printf("\nthe multiplication of %d & %d is %d", x, y, x * y);
    printf("\nthe division of %d & %d is %d", x, y, x / y);
    return 0;
}


